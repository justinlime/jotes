/**
 * search-worker.js – Jotes search runs off the main thread so backend fetches,
 * cancellation, and result normalization do not block keyboard input or page
 * interactions.
 *
 * Protocol (main → worker):
 *   { type: "search", query: "…", id: <number>, mode: "name"|"content"|"auto" }
 *
 * Protocol (worker → main):
 *   { id: <number>, results: [ {type, name, path, line?, text?}, … ], warning?: "…" }
 *   { id: <number>, error: "…" }
 */
"use strict";

var activeSearchController = null;

/**
 * Build the backend URL for one normalized search request.
 *
 * Parameters:
 *   - query: the already-trimmed user query string.
 *   - mode: the normalized search mode to request from the backend.
 *
 * Returns:
 *   - string: the fully encoded /api/search URL for the requested query and mode.
 */
function buildSearchURL(query, mode) {
  return "/api/search?q=" + encodeURIComponent(query) + "&mode=" + encodeURIComponent(mode || "name");
}

/**
 * Cancel the currently running backend request, if any, so stale queries stop
 * consuming worker and server resources as soon as a newer query arrives.
 *
 * Parameters:
 *   - none: the function operates on the worker's shared AbortController state.
 *
 * Returns:
 *   - none: any active request is aborted and cleared.
 */
function abortActiveSearch() {
  if (!activeSearchController) {
    return;
  }

  activeSearchController.abort();
  activeSearchController = null;
}

/**
 * Execute one backend search request and post the normalized response back to
 * the main thread.
 *
 * Parameters:
 *   - query: the already-trimmed user query string.
 *   - id: the logical request identifier generated by the main thread.
 *   - mode: the normalized search mode to request from the backend.
 *
 * Returns:
 *   - none: a result or error payload is posted back to the main thread unless the request was aborted in favor of a newer one.
 */
function performBackendSearch(query, id, mode) {
  var controller = new AbortController();
  var url = buildSearchURL(query, mode);

  abortActiveSearch();
  activeSearchController = controller;

  fetch(url, { signal: controller.signal })
    .then(function (response) {
      if (!response.ok) {
        throw new Error("Search failed with HTTP " + response.status + ".");
      }
      return response.json();
    })
    .then(function (payload) {
      if (activeSearchController === controller) {
        activeSearchController = null;
      }

      self.postMessage({
        id: id,
        results: payload.results || [],
        warning: payload.warning || "",
      });
    })
    .catch(function (error) {
      if (error && error.name === "AbortError") {
        return;
      }

      if (activeSearchController === controller) {
        activeSearchController = null;
      }

      self.postMessage({
        id: id,
        error: error && error.message ? error.message : "Search is temporarily unavailable.",
      });
    });
}

/**
 * Receive search requests from the main thread and forward them to the unified
 * backend search API.
 *
 * Parameters:
 *   - e: the worker message event containing the current query, request ID, and mode.
 *
 * Returns:
 *   - none: the requested backend search is started unless the query is blank.
 */
self.onmessage = function (e) {
  var msg = e.data || {};
  var query = String(msg.query || "").trim();

  if (msg.type !== "search") {
    return;
  }

  if (!query) {
    abortActiveSearch();
    self.postMessage({ id: msg.id, results: [] });
    return;
  }

  performBackendSearch(query, msg.id, msg.mode || "name");
};
